int main() {
    printf("hello world\n");
    int integer = 1;
    long long_number = 123456789011111111111111;
    float float_number = 1.274567890;
    double double_number = 1.274;
    char character = 'c';

    printf("this is an integer %d\n", integer);
    printf("this is a long number %ld\n", long_number);
    printf("this is a float %0.1f\n", float_number);
    printf("this is a double %0.1lf\n", double_number);
    printf("this is a character %c\n", character);
    return 0;
}